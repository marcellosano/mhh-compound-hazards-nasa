def find_existing_runs(base_path):
    """Find existing MHH run folders that can be used as baseline.

    Now detects folders even without config.json by checking for:
    - config.json (preferred), OR
    - Phase2_Thresholds/, OR
    - Phase2_Extremes/, OR
    - Phase3_Clusters/

    Also searches one level deeper (e.g., MHH/MHH/) in case of nested folders.
    """
    # Search in base_path and one level deeper
    search_paths = [
        os.path.join(base_path, f"{Config.OUTPUT_FOLDER_PREFIX}_*"),
        os.path.join(base_path, "*", f"{Config.OUTPUT_FOLDER_PREFIX}_*"),  # One level deeper
    ]

    all_folders = []
    for pattern in search_paths:
        all_folders.extend(glob_module.glob(pattern))

    # Deduplicate and sort (newest first)
    folders = sorted(set(all_folders), reverse=True)

    valid_folders = []
    for folder in folders:
        if not os.path.isdir(folder):
            continue

        # Check for config.json first
        if os.path.exists(os.path.join(folder, "config.json")):
            valid_folders.append(folder)
            continue

        # Also accept folders with pipeline output directories (for interrupted runs)
        has_thresholds = os.path.exists(os.path.join(folder, "Phase2_Thresholds"))
        has_extremes = os.path.exists(os.path.join(folder, "Phase2_Extremes"))
        has_clusters = os.path.exists(os.path.join(folder, "Phase3_Clusters"))

        if has_thresholds or has_extremes or has_clusters:
            valid_folders.append(folder)

    return valid_folders


