    def display(self):
        """Display the progress tracker."""
        display(self.container)

    def start(self):
        """Start the timer."""
        self.start_time = time.time()
        self._update_time_display()

    def start_section(self, section_num, sections_to_run):
        """Mark a section as running."""
        self.current_section = section_num
        self.section_start_time = time.time()
        self.section_states[section_num] = 'running'

        info = SECTION_INFO.get(section_num, {})
        self.section_label.value = f"<b style='color: #3498db;'>🔄 Running: {info.get('name', f'Section {section_num}')}</b>"
        self.section_progress.value = 0
        self.section_progress.bar_style = 'info'

        # Calculate overall progress
        completed = sum(1 for s, state in self.section_states.items()
                       if state in ['complete', 'skipped'] and s in sections_to_run)
        total = len(sections_to_run)
        if total > 0:
            self.overall_progress.value = (completed / total) * 100

        self._update_grid()
        self._update_time_display()

    def complete_section(self, section_num, sections_to_run):
        """Mark a section as complete."""
        self.section_states[section_num] = 'complete'

        info = SECTION_INFO.get(section_num, {})
        elapsed = time.time() - self.section_start_time if self.section_start_time else 0
        self.section_label.value = f"<b style='color: #27ae60;'>✅ Completed: {info.get('name', '')} ({elapsed/60:.1f} min)</b>"
        self.section_progress.value = 100
        self.section_progress.bar_style = 'success'

        # Update overall progress
        completed = sum(1 for s, state in self.section_states.items()
                       if state in ['complete', 'skipped'] and s in sections_to_run)
        total = len(sections_to_run)
        if total > 0:
            self.overall_progress.value = (completed / total) * 100

        self._update_grid()
        self._update_time_display()

    def skip_section(self, section_num, sections_to_run):
        """Mark a section as skipped."""
        self.section_states[section_num] = 'skipped'
        self._update_grid()

        # Update overall progress
        completed = sum(1 for s, state in self.section_states.items()
                       if state in ['complete', 'skipped'] and s in sections_to_run)
        total = len(sections_to_run)
        if total > 0:
            self.overall_progress.value = (completed / total) * 100

    def error_section(self, section_num):
        """Mark a section as errored."""
        self.section_states[section_num] = 'error'
        self.section_label.value = f"<b style='color: #e74c3c;'>❌ Error in Section {section_num}</b>"
        self.section_progress.bar_style = 'danger'
        self._update_grid()

    def complete_all(self):
        """Mark pipeline as complete."""
        self.overall_progress.value = 100
        self.overall_progress.bar_style = 'success'

        total_elapsed = time.time() - self.start_time if self.start_time else 0
        self.overall_label.value = f"<b style='color: #27ae60;'>🎉 Pipeline Complete! Total time: {total_elapsed/60:.1f} minutes</b>"

        self.runtime_warning.value = """
            <div style='background: #d4edda; border: 1px solid #28a745; border-radius: 5px;
                        padding: 10px; margin-top: 10px; font-size: 12px;'>
                <b>✅ All done!</b> Check the outputs folder for your results.
            </div>
        """

    def _update_grid(self):
        """Update the section status grid."""
        self.section_grid.value = self._build_section_grid()

    def _update_time_display(self):
        """Update the elapsed/remaining time display."""
        if not self.start_time:
            return

        elapsed = time.time() - self.start_time
        elapsed_str = str(timedelta(seconds=int(elapsed)))

        # Estimate remaining based on progress
        progress = self.overall_progress.value
        if progress > 0 and progress < 100:
            total_estimated = elapsed / (progress / 100)
            remaining = total_estimated - elapsed
            remaining_str = str(timedelta(seconds=int(remaining)))
        else:
            remaining_str = "--:--:--"

        self.time_info.value = f"""
            <div style='font-size: 12px; color: #7f8c8d; margin-top: 10px;'>
                <b>Elapsed:</b> {elapsed_str} | <b>Est. Remaining:</b> {remaining_str}
            </div>
        """

    def update_section_progress(self, value, message=""):
        """Update the current section's progress (0-100)."""
        self.section_progress.value = value
        if message:
            self.section_label.value = f"<b style='color: #3498db;'>🔄 {message}</b>"
        self._update_time_display()


