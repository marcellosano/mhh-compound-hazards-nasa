    extremes_dir = os.path.join(run_path, "Phase2_Extremes")
    if os.path.exists(extremes_dir):
        extreme_files = glob.glob(os.path.join(extremes_dir, "extremes_*.nc"))
        if len(extreme_files) >= 210:
            result["section_status"][4] = "complete"
        elif len(extreme_files) > 0:
            result["section_status"][4] = "partial"
            result["missing_files"].append(f"Extremes: {len(extreme_files)}/210")
        else:
            result["section_status"][4] = "missing"
    else:
        result["section_status"][4] = "missing"

    # Section 5: Clusters (10 catalogs expected)
    clusters_dir = os.path.join(run_path, "Phase3_Clusters")
    if os.path.exists(clusters_dir):
        catalog_files = glob.glob(os.path.join(clusters_dir, "cluster_catalog_*.csv"))
        if len(catalog_files) >= 10:
            result["section_status"][5] = "complete"
        elif len(catalog_files) > 0:
            result["section_status"][5] = "partial"
            result["missing_files"].append(f"Clusters: {len(catalog_files)}/10")
        else:
            result["section_status"][5] = "missing"
    else:
        result["section_status"][5] = "missing"

    # Section 6: Multi-hazard (3 files expected)
    footprints_dir = os.path.join(run_path, "Phase4_Footprints")
    if os.path.exists(footprints_dir):
        mh_files = glob.glob(os.path.join(footprints_dir, "multihazard_*.csv"))
        if len(mh_files) >= 3:
            result["section_status"][6] = "complete"
        elif len(mh_files) > 0:
            result["section_status"][6] = "partial"
            result["missing_files"].append(f"Multi-hazard: {len(mh_files)}/3")
        else:
            result["section_status"][6] = "missing"
    else:
        result["section_status"][6] = "missing"

    # Determine resume point (Sections 3-6 only, analysis done externally)
    for section in [3, 4, 5, 6]:
        status = result["section_status"].get(section, "missing")
        if status in ["missing", "partial"]:
            result["resume_from"] = section
            break

    # Check if fully complete (through Section 6)
    result["is_complete"] = all(
        result["section_status"].get(s) == "complete"
        for s in [3, 4, 5, 6]
    )

    return result


