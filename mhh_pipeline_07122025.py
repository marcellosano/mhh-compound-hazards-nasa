class DBSCANClustering:
    """Section 5: Cluster extreme events using DBSCAN."""

    @staticmethod
    def get_files_for_variable(var_key, extremes_dir):
        """
        Get extreme files for a specific variable with exact matching.
        Returns list of (filepath, scenario, decade) tuples.
        """
        all_files = glob.glob(os.path.join(extremes_dir, "extremes_*.nc"))

        matched_files = []
        for f in all_files:
            filename = os.path.basename(f)

            # Format: extremes_{variable}_{scenario}_{start_year}_{end_year}.nc
            parts = filename.replace("extremes_", "").replace(".nc", "").split("_")

            # Find where scenario starts
            scenario_idx = -1
            for i, part in enumerate(parts):
                if part in Config.SCENARIOS:
                    scenario_idx = i
                    break

            if scenario_idx == -1:
                continue

            # Variable name is everything before scenario
            file_var = "_".join(parts[:scenario_idx])

            # Exact match only
            if file_var == var_key:
                scenario = parts[scenario_idx]
                # Get decade (last two parts before .nc)
                if len(parts) >= scenario_idx + 3:
                    decade = f"{parts[scenario_idx+1]}_{parts[scenario_idx+2]}"
                else:
                    decade = "unknown"

                matched_files.append((f, scenario, decade))

        return sorted(matched_files)

    @staticmethod
    def convert_times_to_days(time_vals):
        """
        Convert time array to days since epoch efficiently.
        Returns array of days and list of pandas Timestamps.
        """
        time_pd_list = []
        time_days_list = []

        for t in time_vals:
            try:
                time_pd = pd.Timestamp(t)
            except:
                try:
                    time_pd = pd.Timestamp(str(t))
                except:
                    # Use placeholder for invalid timestamps
                    time_pd = pd.Timestamp('1970-01-01')
                    print(f"⚠️  Warning: Could not parse timestamp: {t}")

            time_pd_list.append(time_pd)
            time_days = (time_pd - pd.Timestamp('1970-01-01')).days
            time_days_list.append(time_days)

        return np.array(time_days_list), time_pd_list

    @staticmethod
    def cluster_single_file(filepath, scenario, decade, var_key, config):
        """
        Cluster extremes from a single file.
        Returns list of cluster records or None if no clusters.

        FIXED: Handles H08 model files with non-standard time encoding (days since 1601-01-01).
        """
        EPS_SPACE_DEG = config["eps_space_deg"]
        EPS_TIME_DAYS = config["eps_time_days"]
        MIN_SAMPLES = config["min_samples"]

        try:
            # ================================================================
            # FIX: Try normal open first, fall back to decode_times=False
            # Guarded by config flag for dataset-specific workarounds
            # ================================================================
            h08_fallback = False
            h08_workaround_enabled = Config.FILE_NAMING.get(
                'time_decode_workarounds', {}).get('h08_1601_epoch', False)
            try:
                da_extremes = xr.open_dataarray(filepath)
            except ValueError as e:
                if h08_workaround_enabled and (
                    "unable to decode time" in str(e) or "1601-01-01" in str(e)
                ):
                    # H08 model files have non-standard time encoding
                    h08_fallback = True

                    # Open without decoding times
                    ds = xr.open_dataset(filepath, decode_times=False)
                    var_name = [v for v in ds.data_vars][0]
                    da_extremes = ds[var_name]

                    # Reconstruct time coordinate from filename
                    # decade format: "1981_1990" or similar
                    start_year = int(decade.split('_')[0])
                    n_times = da_extremes.shape[0]

                    # Create date range for the period
                    date_range = pd.date_range(
                        start=f'{start_year}-01-01',
                        periods=n_times,
                        freq='D'
                    )

                    # Assign the reconstructed time coordinate
                    da_extremes = da_extremes.assign_coords(time=date_range)
                else:
                    raise

            # Find extreme points (where value == 1)
            extreme_coords = np.argwhere(da_extremes.values == 1)
            n_extremes = len(extreme_coords)

            print(f"{n_extremes:8,} pts", end="")

            if n_extremes == 0 or n_extremes < MIN_SAMPLES:
                print(" → skip (too few)")
                da_extremes.close()
                return None

            # Extract coordinate arrays ONCE
            time_vals = da_extremes.time.values
            lat_vals = da_extremes.lat.values
            lon_vals = da_extremes.lon.values

            # Convert time array to days ONCE (major speedup!)
            time_days_arr, time_pd_arr = DBSCANClustering.convert_times_to_days(time_vals)

            # Build feature matrix efficiently
            features = np.array([
                [
                    lat_vals[lat_idx] / EPS_SPACE_DEG,
                    lon_vals[lon_idx] / EPS_SPACE_DEG,
                    time_days_arr[t_idx] / EPS_TIME_DAYS
                ]
                for t_idx, lat_idx, lon_idx in extreme_coords
            ])

            # Run DBSCAN clustering
            dbscan = DBSCAN(eps=1.0, min_samples=MIN_SAMPLES, metric='euclidean', n_jobs=1)
            labels = dbscan.fit_predict(features)

            # Count clusters and noise
            n_clusters = len(set(labels)) - (1 if -1 in labels else 0)
            n_noise = np.sum(labels == -1)

            print(f" → {n_clusters:4d} clusters ({100*n_noise/len(features):4.1f}% noise)")

            if n_clusters == 0:
                da_extremes.close()
                return None

            # Get max_gap_days for gap-splitting
            MAX_GAP_DAYS = config.get("max_gap_days", 7)

            # Extract cluster properties with gap-splitting
            cluster_records = []

            for cluster_id in set(labels):
                if cluster_id == -1:  # Skip noise
                    continue

                cluster_mask = (labels == cluster_id)
                cluster_coords = extreme_coords[cluster_mask]

                # Get coordinates for this cluster
                times_pd = np.array([time_pd_arr[t_idx] for t_idx, _, _ in cluster_coords])
                lat_indices = np.array([lat_idx for _, lat_idx, _ in cluster_coords])
                lon_indices = np.array([lon_idx for _, _, lon_idx in cluster_coords])
                lats = lat_vals[lat_indices]
                lons = lon_vals[lon_indices]

                # Sort by time for gap detection
                time_order = np.argsort(times_pd)
                times_pd_sorted = times_pd[time_order]
                lats_sorted = lats[time_order]
                lons_sorted = lons[time_order]

                # Detect gaps and split into sub-clusters
                sub_clusters = []
                current_start_idx = 0

                for i in range(1, len(times_pd_sorted)):
                    gap_days = (times_pd_sorted[i] - times_pd_sorted[i-1]).days
                    if gap_days > MAX_GAP_DAYS:
                        # End current sub-cluster, start new one
                        sub_clusters.append((current_start_idx, i))
                        current_start_idx = i

                # Add final sub-cluster
                sub_clusters.append((current_start_idx, len(times_pd_sorted)))

                # Create records for each sub-cluster
                for sub_idx, (start_idx, end_idx) in enumerate(sub_clusters):
                    sub_times = times_pd_sorted[start_idx:end_idx]
                    sub_lats = lats_sorted[start_idx:end_idx]
                    sub_lons = lons_sorted[start_idx:end_idx]

                    # Calculate temporal extent
                    start_time = sub_times[0]
                    end_time = sub_times[-1]
                    duration = (end_time - start_time).days + 1

                    # Calculate spatial extent - FIXED: use grid cell count
                    center_lat = float(np.mean(sub_lats))
                    center_lon = float(np.mean(sub_lons))
                    n_unique_cells = len(set(zip(sub_lats, sub_lons)))
                    cell_area_km2 = (Config.GRID_RESOLUTION_DEG * 111) ** 2 * np.cos(np.radians(center_lat))
                    extent_km2 = n_unique_cells * cell_area_km2

                    # Create unique, stable cluster ID using decade
                    # Use suffix for split clusters (a, b, c, ...)
                    if len(sub_clusters) == 1:
                        unique_id = f"{var_key}_{scenario}_{decade}_{cluster_id:04d}"
                    else:
                        suffix = chr(ord('a') + sub_idx)  # a, b, c, ...
                        unique_id = f"{var_key}_{scenario}_{decade}_{cluster_id:04d}{suffix}"

                    # Store cluster record with CONSISTENT schema
                    cluster_records.append({
                        'variable': var_key,
                        'cluster_id': unique_id,
                        'scenario': scenario,
                        'start_day': start_time.strftime('%Y-%m-%d'),
                        'end_day': end_time.strftime('%Y-%m-%d'),
                        'duration_days': int(duration),
                        'n_gridpoints': int(len(sub_times)),
                        'center_lat': center_lat,
                        'center_lon': center_lon,
                        'spatial_extent_km2': float(extent_km2)
                    })

            # Clean up
            da_extremes.close()
            if h08_fallback:
                ds.close()
            del da_extremes, extreme_coords, features, labels
            gc.collect()

            return cluster_records

        except Exception as e:
            print(f" ❌ Error: {str(e)[:50]}")
            import traceback
            traceback.print_exc()
            return None

