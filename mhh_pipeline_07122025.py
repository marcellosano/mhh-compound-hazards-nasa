            f.write("MHH DBSCAN PARAMETER CONFIGURATION\n")
            f.write(f"Created: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")
            f.write("=" * 70 + "\n\n")

            f.write("VARIABLE CONFIGURATIONS:\n")
            f.write("-" * 50 + "\n")
            for var_name, var_cfg in config["variables"].items():
                f.write(f"\n{var_name} ({var_cfg.get('long_name', '')}):\n")
                f.write(f"  Percentile: {var_cfg.get('percentile_strict')}\n")
