    def _resume_in_baseline(self, button):
        """
        Resume an incomplete run in the SAME baseline folder.
        No new folder is created - work continues in the selected baseline.
        """
        with self.output_area:
            clear_output()

            baseline_path = self.baseline_dropdown.value
            if not baseline_path or not os.path.exists(baseline_path):
                print("❌ Please select a valid baseline run to resume.")
                return

            # Check completeness
            completeness = check_run_completeness(baseline_path)

            print("=" * 70)
            print("🔄 RESUME IN BASELINE FOLDER")
            print("=" * 70)
            print(f"\n📁 Resuming in: {os.path.basename(baseline_path)}")

            # Show status
            print("\n📊 Section Status:")
            status_icons = {'complete': '✅', 'partial': '🔶', 'missing': '❌'}
            for section in [3, 4, 5, 6]:
                status = completeness['section_status'].get(section, 'missing')
                icon = status_icons.get(status, '❓')
                print(f"   Section {section} ({SECTION_INFO[section]['name']}): {icon} {status}")

            if completeness['is_complete']:
                print("\n✅ This run is already complete! Nothing to resume.")
                return

            resume_from = completeness['resume_from']
            print(f"\n▶️  Will resume from Section {resume_from}")

            # Create and display progress tracker
            self.progress_tracker = ProgressTracker()
            self.progress_tracker.display()
            self.progress_tracker.start()

            # Load and apply config from baseline (or create one if missing)
            config_path = os.path.join(baseline_path, "config.json")
            if os.path.exists(config_path):
                with open(config_path, 'r') as f:
                    baseline_config = json.load(f)
                self._apply_config_to_pipeline(baseline_config, baseline_path)
                print("✓ Configuration loaded from baseline")
            else:
                # No config.json - use current widget values and save them
                print("⚠ No config.json found - using current widget values")
                new_config = self._get_current_widget_values()
                self._apply_config_to_pipeline(new_config, baseline_path)
                # Save config so future runs can use it
                self._save_config_files(new_config, baseline_path)
                print("✓ Configuration saved to baseline folder")

            # Mark completed sections
            sections_to_run = []
            for section in [2, 3, 4, 5, 6]:
                status = completeness['section_status'].get(section, 'missing')
                if status == 'complete':
                    self.progress_tracker.skip_section(section, [])
                elif section >= resume_from:
                    sections_to_run.append(section)

            try:
                # Run remaining sections
                for section in sections_to_run:
                    self.progress_tracker.start_section(section, sections_to_run)

                    print(f"\n{'='*70}")
                    print(f"RUNNING SECTION {section}: {SECTION_INFO[section]['name'].upper()}")
                    print(f"Estimated time: {SECTION_INFO[section]['est_time']}")
                    print('='*70)

                    self._run_section(section)
                    self.progress_tracker.complete_section(section, sections_to_run)

                self.progress_tracker.complete_all()

                print("\n" + "=" * 70)
                print("🎉 RESUME COMPLETED!")
                print("=" * 70)
                print(f"\n📁 Outputs: {baseline_path}")

            except Exception as e:
                if self.progress_tracker and self.progress_tracker.current_section:
                    self.progress_tracker.error_section(self.progress_tracker.current_section)
                print(f"\n❌ ERROR: {e}")
                import traceback
                traceback.print_exc()

