class MultiHazardDetection:
    """Section 6: Detect multi-hazard compound events using optimized spatial/temporal matching."""

    @staticmethod
    def haversine_distance_vectorized(lat1, lon1, lat2, lon2):
        """Vectorized haversine distance calculation."""
        R = 6371  # Earth radius in km

        lat1_rad = np.radians(lat1)
        lat2_rad = np.radians(lat2)
        dlat = np.radians(lat2 - lat1)
        dlon = np.radians(lon2 - lon1)

        a = np.sin(dlat/2)**2 + np.cos(lat1_rad) * np.cos(lat2_rad) * np.sin(dlon/2)**2
        c = 2 * np.arctan2(np.sqrt(a), np.sqrt(1-a))

        return R * c

    @staticmethod
    def create_spatiotemporal_index(df, time_window_days=30):
        """
        Create spatial and temporal bins for fast lookup.
        Returns DataFrame with added columns: time_bin, lat_bin, lon_bin
        """
        df = df.copy()

        # Temporal binning (by month)
        df['time_bin'] = df['start_date'].dt.to_period('M')

        # Spatial binning (2° grid cells for coarse filtering)
        df['lat_bin'] = (df['center_lat'] / 2).astype(int)
        df['lon_bin'] = (df['center_lon'] / 2).astype(int)

        return df

    @staticmethod
    def find_overlapping_events_optimized(primary_df, conditioning_df, time_lag_days, max_distance_km):
        """
        Find overlapping events using spatial/temporal indexing.
        Much faster than nested loops - uses binning and KD-tree approach.
        """
        # Add spatial/temporal bins
        primary_indexed = MultiHazardDetection.create_spatiotemporal_index(primary_df)
        conditioning_indexed = MultiHazardDetection.create_spatiotemporal_index(conditioning_df)

        matches = []

        # Group by time bins (coarse temporal filter)
        primary_groups = primary_indexed.groupby('time_bin')

        print(f"      Processing {len(primary_groups)} time bins...")

        for time_bin, primary_group in primary_groups:
            # Get conditioning events in nearby time bins (±time_lag)
            nearby_time_bins = []
            for offset in range(-time_lag_days // 30 - 1, time_lag_days // 30 + 2):
                nearby_bin = time_bin + offset
                nearby_time_bins.append(nearby_bin)

            cond_candidates = conditioning_indexed[
                conditioning_indexed['time_bin'].isin(nearby_time_bins)
            ]

            if len(cond_candidates) == 0:
                continue

            # Spatial filtering using nearby grid cells
            for _, primary in primary_group.iterrows():
                # Get conditioning events in nearby spatial bins
                lat_bins = [primary['lat_bin'] - 1, primary['lat_bin'], primary['lat_bin'] + 1]
                lon_bins = [primary['lon_bin'] - 1, primary['lon_bin'], primary['lon_bin'] + 1]

                spatial_candidates = cond_candidates[
                    (cond_candidates['lat_bin'].isin(lat_bins)) &
                    (cond_candidates['lon_bin'].isin(lon_bins))
                ]

                if len(spatial_candidates) == 0:
                    continue

                # Fine-grained temporal check (vectorized)
                extended_start = primary['start_date'] - timedelta(days=time_lag_days)
                extended_end = primary['end_date'] + timedelta(days=time_lag_days)

                temporal_mask = (
                    (spatial_candidates['start_date'] <= extended_end) &
                    (spatial_candidates['end_date'] >= extended_start)
                )

                temporal_candidates = spatial_candidates[temporal_mask]

                if len(temporal_candidates) == 0:
                    continue

                # Fine-grained spatial check (vectorized)
                distances = MultiHazardDetection.haversine_distance_vectorized(
                    primary['center_lat'],
                    primary['center_lon'],
                    temporal_candidates['center_lat'].values,
                    temporal_candidates['center_lon'].values
                )

                spatial_mask = distances <= max_distance_km
                final_matches = temporal_candidates[spatial_mask]

                if len(final_matches) > 0:
                    matches.append({
                        'primary_idx': primary.name,
                        'n_conditioning': len(final_matches),
                        'conditioning_variables': ','.join(final_matches['variable'].unique())
                    })

        return pd.DataFrame(matches) if matches else None

