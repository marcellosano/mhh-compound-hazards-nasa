def detect_changes(old_config, new_config):
    """Detect what changed between two configurations."""

    changes = {"thresholds": [], "dbscan": [], "multi_hazard": []}

    for var_name in new_config["variables"]:
        old_var = old_config["variables"].get(var_name, {})
        new_var = new_config["variables"][var_name]

        # Handle fixed_threshold specially - only compare if actually used
        old_use_fixed = old_var.get("use_fixed_as_minimum", False)
        new_use_fixed = new_var.get("use_fixed_as_minimum", False)

        # Check percentile_strict
        old_pct = old_var.get("percentile_strict")
        new_pct = new_var.get("percentile_strict")
        if old_pct != new_pct:
            changes["thresholds"].append(f"{var_name}.percentile: {old_pct} → {new_pct}")

        # Check use_fixed_as_minimum
        if old_use_fixed != new_use_fixed:
            changes["thresholds"].append(f"{var_name}.use_fixed: {old_use_fixed} → {new_use_fixed}")

        # Only check fixed_threshold if it's being used
        if old_use_fixed or new_use_fixed:
            old_fixed = old_var.get("fixed_threshold")
            new_fixed = new_var.get("fixed_threshold")
            if old_fixed != new_fixed:
                changes["thresholds"].append(f"{var_name}.fixed_threshold: {old_fixed} → {new_fixed}")

        # Check DBSCAN params
        old_dbscan = old_var.get("dbscan", {})
        new_dbscan = new_var.get("dbscan", {})
        for param in DEPENDENCY_CHAIN["dbscan"]["params"]:
            old_val = old_dbscan.get(param)
            new_val = new_dbscan.get(param)
            if old_val != new_val:
                changes["dbscan"].append(f"{var_name}.{param}: {old_val} → {new_val}")

    # Check multi-hazard rules
    for rule_name in new_config["multi_hazard_rules"]:
        old_rule = old_config["multi_hazard_rules"].get(rule_name, {})
        new_rule = new_config["multi_hazard_rules"][rule_name]

        if old_rule.get("time_lag_days") != new_rule.get("time_lag_days"):
            changes["multi_hazard"].append(f"{rule_name}.time_lag_days: {old_rule.get('time_lag_days')} → {new_rule.get('time_lag_days')}")

        old_primary = old_rule.get("primary", {}).get("min_required")
        new_primary = new_rule.get("primary", {}).get("min_required")
        if old_primary != new_primary:
            changes["multi_hazard"].append(f"{rule_name}.primary_min: {old_primary} → {new_primary}")

        old_cond = old_rule.get("conditioning", {}).get("min_required")
        new_cond = new_rule.get("conditioning", {}).get("min_required")
        if old_cond != new_cond:
            changes["multi_hazard"].append(f"{rule_name}.cond_min: {old_cond} → {new_cond}")

    # Determine rerun_from
    rerun_from = None
    if changes["thresholds"]:
        rerun_from = 3
    elif changes["dbscan"]:
        rerun_from = 5
    elif changes["multi_hazard"]:
        rerun_from = 6

    all_changes = changes["thresholds"] + changes["dbscan"] + changes["multi_hazard"]
    return rerun_from, all_changes, changes


