    def _get_current_widget_values(self):
        """Extract current configuration from widgets."""
        config = deepcopy(self.config)

        for var_name, widgets_dict in self.variable_widgets.items():
            config["variables"][var_name]["percentile_strict"] = widgets_dict["percentile_strict"].value

            # Only store fixed_threshold if use_fixed_as_minimum is checked
            if widgets_dict.get("_has_fixed", False):
                use_fixed = widgets_dict["use_fixed_as_minimum"].value
                config["variables"][var_name]["use_fixed_as_minimum"] = use_fixed
                if use_fixed:
                    config["variables"][var_name]["fixed_threshold"] = widgets_dict["fixed_threshold"].value
                else:
                    config["variables"][var_name]["fixed_threshold"] = None
            else:
                config["variables"][var_name]["use_fixed_as_minimum"] = False
                config["variables"][var_name]["fixed_threshold"] = None

            config["variables"][var_name]["dbscan"]["eps_space_deg"] = widgets_dict["eps_space_deg"].value
            config["variables"][var_name]["dbscan"]["eps_time_days"] = widgets_dict["eps_time_days"].value
            config["variables"][var_name]["dbscan"]["min_samples"] = widgets_dict["min_samples"].value
            config["variables"][var_name]["dbscan"]["max_gap_days"] = widgets_dict["max_gap_days"].value

        for rule_name, widgets_dict in self.mh_widgets.items():
            config["multi_hazard_rules"][rule_name]["time_lag_days"] = widgets_dict["time_lag_days"].value

            # All rules now use primary/conditioning structure (including windstorm)
            if "primary" not in config["multi_hazard_rules"][rule_name]:
                # Initialize from MULTI_HAZARD_RULES if missing
                config["multi_hazard_rules"][rule_name]["primary"] = {
                    "variables": Config.MULTI_HAZARD_RULES[rule_name]["primary"]["variables"],
                    "min_required": 1
                }
            if "conditioning" not in config["multi_hazard_rules"][rule_name]:
                config["multi_hazard_rules"][rule_name]["conditioning"] = {
                    "variables": Config.MULTI_HAZARD_RULES[rule_name]["conditioning"]["variables"],
                    "min_required": 1
                }

            # Update from widget values
            config["multi_hazard_rules"][rule_name]["primary"]["min_required"] = widgets_dict["primary_min_required"].value
            config["multi_hazard_rules"][rule_name]["conditioning"]["min_required"] = widgets_dict["conditioning_min_required"].value

        return config

    def _save_config_files(self, config, output_path):
        """Save configuration files."""
