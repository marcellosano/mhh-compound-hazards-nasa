def load_urls_from_excel(file_path: str) -> pd.DataFrame:
    """
    Load URLs and metadata from Excel file.

    Args:
        file_path: Path to Excel file

    Returns:
        DataFrame with URL data
    """
    logger = logging.getLogger('ISIMIP_Downloader')

    try:
        logger.info(f"Loading URLs from {file_path}")
        df = pd.read_excel(file_path)

        # Validate required columns
        required_cols = ['Placeholder_Download_Link', 'Dataset']
        missing_cols = [col for col in required_cols if col not in df.columns]

        if missing_cols:
            raise ValueError(f"Missing required columns: {missing_cols}")

        logger.info(f"Loaded {len(df)} URLs from Excel file")
        return df

    except Exception as e:
        logger.error(f"Error loading Excel file: {e}")
        raise

def get_filename_from_url(url: str, dataset_name: str) -> str:
    """
    Generate filename from URL and dataset info.

    Args:
        url: Download URL
        dataset_name: Dataset name from Excel

    Returns:
        Filename for saving
    """
    # Extract UUID from URL
    parsed = urlparse(url)
    uuid = parsed.path.split('/')[-2] if parsed.path.endswith('/') else parsed.path.split('/')[-1]

    # Create filename: dataset_uuid.nc
    filename = f"{dataset_name}_{uuid}.nc"

    # Clean filename (remove any invalid characters)
    invalid_chars = '<>:"|?*'
    for char in invalid_chars:
        filename = filename.replace(char, '_')

    return filename

