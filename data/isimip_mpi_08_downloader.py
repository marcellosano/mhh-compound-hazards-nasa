def estimate_download_size(df: pd.DataFrame) -> None:
    """
    Estimate total download size based on a sample.

    Args:
        df: DataFrame with dataset information
    """
    print("\n📊 Estimating total download size...")
    print("(This is a rough estimate based on checking a few files)")

    session = requests.Session()
    session.headers.update(HEADERS)

    # Sample 5 random datasets
    sample_size = min(5, len(df))
    sample_df = df.sample(n=sample_size)

    total_size = 0
    successful_checks = 0

    for _, row in sample_df.iterrows():
        try:
            # Find download link
            download_url = find_nc_download_link(row['Placeholder_Download_Link'], session)
            if download_url:
                # Get file size without downloading
                response = session.head(download_url, timeout=10)
                if response.status_code == 200:
                    file_size = int(response.headers.get('content-length', 0))
                    total_size += file_size
                    successful_checks += 1
                    print(f"  • {row['Dataset']}: {file_size/(1024**2):.1f} MB")
            time.sleep(1)  # Be nice to the server
        except:
            pass

    session.close()

    if successful_checks > 0:
        avg_size = total_size / successful_checks
        estimated_total = (avg_size * len(df)) / (1024**3)  # Convert to GB
        print(f"\n📈 Estimated total download size: ~{estimated_total:.1f} GB")
        print(f"   (Based on {successful_checks} samples, ~{avg_size/(1024**2):.1f} MB per file)")
    else:
        print("❌ Could not estimate download size")

