def download_file(url: str, filepath: Path, session: requests.Session) -> bool:
    """
    Download a file with progress bar and resume capability.

    Args:
        url: Download URL
        filepath: Path where to save the file
        session: Requests session object

    Returns:
        True if successful, False otherwise
    """
    logger = logging.getLogger('ISIMIP_Downloader')

    # Check if file already exists
    if filepath.exists():
        existing_size = filepath.stat().st_size
        headers = {'Range': f'bytes={existing_size}-'}
        mode = 'ab'
        logger.info(f"Resuming download from byte {existing_size}")
    else:
        headers = {}
        mode = 'wb'
        existing_size = 0

    try:
        # Add delay
        time.sleep(CONFIG['delay_between_requests'])

        # Make request
        response = session.get(url, headers=headers, stream=True, timeout=CONFIG['timeout'])
        response.raise_for_status()

        # Get total file size
        total_size = int(response.headers.get('content-length', 0)) + existing_size

        # Download with progress bar
        with open(filepath, mode) as f:
            with tqdm(total=total_size, initial=existing_size,
                     unit='B', unit_scale=True, desc=filepath.name) as pbar:
                for chunk in response.iter_content(chunk_size=CONFIG['chunk_size']):
                    if chunk:
                        f.write(chunk)
                        pbar.update(len(chunk))

        logger.info(f"Successfully downloaded: {filepath.name}")
        return True

    except Exception as e:
        logger.error(f"Error downloading {url}: {e}")
        return False

def download_with_retry(url: str, filepath: Path, session: requests.Session) -> bool:
    """
    Download file with retry logic.

    Args:
        url: Download URL
        filepath: Path where to save the file
        session: Requests session object

    Returns:
        True if successful, False otherwise
    """
    logger = logging.getLogger('ISIMIP_Downloader')

    for attempt in range(CONFIG['max_retries']):
        if attempt > 0:
            logger.info(f"Retry attempt {attempt} for {filepath.name}")
            time.sleep(5 * attempt)  # Exponential backoff

        if download_file(url, filepath, session):
            return True

    logger.error(f"Failed to download {filepath.name} after {CONFIG['max_retries']} attempts")
    return False

