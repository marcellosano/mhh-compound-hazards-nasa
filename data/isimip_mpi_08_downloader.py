def find_nc_download_link(page_url: str, session: requests.Session) -> Optional[str]:
    """
    Scrape ISIMIP page to find the .nc file download link.

    Args:
        page_url: URL of the ISIMIP page
        session: Requests session object

    Returns:
        Direct download URL for .nc file, or None if not found
    """
    logger = logging.getLogger('ISIMIP_Downloader')

    try:
        # Add delay to be respectful to the server
        time.sleep(CONFIG['delay_between_requests'])

        # Get the page
        response = session.get(page_url, timeout=CONFIG['timeout'])
        response.raise_for_status()

        # Parse HTML
        soup = BeautifulSoup(response.text, 'html.parser')

        # Strategy 1: Look for direct download links
        # Search for links containing .nc extension
        nc_links = []
        for link in soup.find_all('a', href=True):
            href = link['href']
            if '.nc' in href:
                nc_links.append(href)

        # Strategy 2: Look for download buttons or specific classes
        # (This may need adjustment based on actual ISIMIP HTML structure)
        download_buttons = soup.find_all('a', class_=['download', 'btn-download', 'download-link'])
        for btn in download_buttons:
            if 'href' in btn.attrs:
                nc_links.append(btn['href'])

        # Strategy 3: Look for links with specific patterns
        # ISIMIP might use specific URL patterns for downloads
        for link in soup.find_all('a', href=True):
            href = link['href']
            if any(pattern in href for pattern in ['download', 'files', '.nc']):
                nc_links.append(href)

        # Remove duplicates and filter for .nc files
        nc_links = list(set(nc_links))
        nc_links = [link for link in nc_links if link.endswith('.nc')]

        if nc_links:
            # Convert relative URLs to absolute
            download_url = urljoin(page_url, nc_links[0])
            logger.info(f"Found .nc download link: {download_url}")
            return download_url
        else:
            logger.warning(f"No .nc download link found on page: {page_url}")
            return None

    except requests.RequestException as e:
        logger.error(f"Error accessing page {page_url}: {e}")
        return None
    except Exception as e:
        logger.error(f"Unexpected error while scraping {page_url}: {e}")
        return None

