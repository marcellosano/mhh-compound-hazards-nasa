def process_single_dataset(row: pd.Series, download_path: Path, session: requests.Session) -> Dict:
    """
    Process a single dataset: scrape and download.

    Args:
        row: DataFrame row with dataset info
        download_path: Path to download folder
        session: Requests session object

    Returns:
        Dictionary with processing results
    """
    logger = logging.getLogger('ISIMIP_Downloader')

    result = {
        'dataset': row['Dataset'],
        'page_url': row['Placeholder_Download_Link'],
        'status': 'failed',
        'download_url': None,
        'filepath': None,
        'error': None
    }

    try:
        # Step 1: Find the .nc download link
        logger.info(f"Processing: {row['Dataset']}")
        download_url = find_nc_download_link(row['Placeholder_Download_Link'], session)

        if not download_url:
            result['error'] = 'No .nc download link found'
            return result

        result['download_url'] = download_url

        # Step 2: Generate filename and download
        filename = get_filename_from_url(download_url, row['Dataset'])
        filepath = download_path / filename
        result['filepath'] = str(filepath)

        # Step 3: Download the file
        if download_with_retry(download_url, filepath, session):
            result['status'] = 'success'
        else:
            result['error'] = 'Download failed'

    except Exception as e:
        logger.error(f"Error processing {row['Dataset']}: {e}")
        result['error'] = str(e)

    return result

def process_all_datasets(df: pd.DataFrame, download_path: Path, parallel: bool = False) -> List[Dict]:
    """
    Process all datasets from the DataFrame.

    Args:
        df: DataFrame with dataset information
        download_path: Path to download folder
        parallel: Whether to use parallel downloads

    Returns:
        List of processing results
    """
    logger = logging.getLogger('ISIMIP_Downloader')
    results = []

    # Create session for connection pooling
    session = requests.Session()
    session.headers.update(HEADERS)

    if parallel:
        logger.info(f"Starting parallel download with {CONFIG['max_workers']} workers")

        with ThreadPoolExecutor(max_workers=CONFIG['max_workers']) as executor:
            # Submit all tasks
            future_to_row = {
                executor.submit(process_single_dataset, row, download_path, session): idx
                for idx, row in df.iterrows()
            }

            # Process completed tasks with progress bar
            with tqdm(total=len(df), desc="Overall Progress") as pbar:
                for future in as_completed(future_to_row):
                    result = future.result()
                    results.append(result)
                    pbar.update(1)
    else:
        logger.info("Starting sequential download")

        # Process sequentially with progress bar
        for idx, row in tqdm(df.iterrows(), total=len(df), desc="Overall Progress"):
            result = process_single_dataset(row, download_path, session)
            results.append(result)

    session.close()
    return results

