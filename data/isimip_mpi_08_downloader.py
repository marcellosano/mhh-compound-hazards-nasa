        print("This could take several hours and use significant bandwidth")
        print("\nOptions:")
        print("1. Download ALL files")
        print("2. Download first 10 files only")
        print("3. Download first N files (you choose)")
        print("4. Exit")

        choice = input("\nEnter your choice (1-4): ").strip()

        if choice == '1':
            df_to_process = df
        elif choice == '2':
            df_to_process = df.head(10)
