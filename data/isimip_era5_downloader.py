def get_file_size_mb(filepath: Path) -> float:
    if filepath.exists():
        return filepath.stat().st_size / (1024 * 1024)
    return 0.0

def get_variable_key(filename: str) -> str:
    """
    Extract variable key for grouping files.
    E.g., '20crv3-era5_obsclim_pr_global_daily_2011_2020.nc' -> '20crv3-era5_obsclim_pr'
          'h08_20crv3-era5_obsclim_1901soc_default_dis_global_daily_2011_2020.nc' -> 'h08_20crv3-era5_obsclim_1901soc_default_dis'
    """
    # Remove extension
    name = filename.replace('.nc', '')

    # Remove year range at end (e.g., _2011_2020, _2021_2021)
    parts = name.rsplit('_', 2)
    if len(parts) >= 3 and parts[-2].isdigit() and parts[-1].isdigit():
        name = '_'.join(parts[:-2])

    # Remove 'global_daily' to get the variable key
    name = name.replace('_global_daily', '')

    return name

def generate_output_name(var_key: str) -> str:
    """Generate output filename from variable key."""
    return f"{var_key}_europe_daily_2018_2021.nc"

def subset_spatial(ds, lat_name: str, lon_name: str):
    """Subset to Europe bounding box."""
    lat_vals = ds[lat_name].values
    if lat_vals[0] > lat_vals[-1]:  # Descending
        lat_slice = slice(CONFIG.LAT_MAX, CONFIG.LAT_MIN)
    else:  # Ascending
        lat_slice = slice(CONFIG.LAT_MIN, CONFIG.LAT_MAX)

    return ds.sel(**{
        lat_name: lat_slice,
        lon_name: slice(CONFIG.LON_MIN, CONFIG.LON_MAX)
    })

def subset_temporal(ds):
    """Subset to target time period."""
    if 'time' in ds.dims:
        return ds.sel(time=slice(CONFIG.TIME_START, CONFIG.TIME_END))
    return ds

def process_variable_group(var_key: str, files: list, output_dir: Path) -> dict:
    """Process a group of files for same variable, merge time periods."""

    result = {
        "variable": var_key,
        "input_files": [f.name for f in files],
        "input_size_mb": sum(get_file_size_mb(f) for f in files),
        "output_size_mb": 0.0,
        "success": False,
        "message": ""
    }

    output_name = generate_output_name(var_key)
    output_path = output_dir / output_name

    try:
        print(f"\n  Processing: {var_key}")
        for f in files:
            print(f"    - {f.name} ({get_file_size_mb(f):.0f} MB)")

        datasets = []

        for fpath in sorted(files):
            print(f"    Loading {fpath.name}...", end=" ", flush=True)
            ds = xr.open_dataset(fpath)

            # Get coord names
            lat_name = 'lat' if 'lat' in ds.coords else 'latitude'
            lon_name = 'lon' if 'lon' in ds.coords else 'longitude'

            # Subset spatial
            ds = subset_spatial(ds, lat_name, lon_name)

            # Subset temporal
            ds = subset_temporal(ds)

            if 'time' in ds.dims and len(ds.time) > 0:
                print(f"{len(ds.time)} days")
                datasets.append(ds)
            else:
                print("no data in range, skipping")
                ds.close()

        if not datasets:
            result["message"] = "No data in target time range"
            return result

        # Merge time periods
        print(f"    Merging {len(datasets)} datasets...", end=" ", flush=True)

        if len(datasets) == 1:
            merged = datasets[0]
        else:
            merged = xr.concat(datasets, dim='time')
            merged = merged.sortby('time')

        # Remove duplicates if any
        if 'time' in merged.dims:
            _, unique_idx = np.unique(merged.time.values, return_index=True)
            if len(unique_idx) < len(merged.time):
                merged = merged.isel(time=sorted(unique_idx))

        print(f"total {len(merged.time)} days")

        # Save with compression
        print(f"    Saving: {output_name}...", end=" ", flush=True)
        output_dir.mkdir(parents=True, exist_ok=True)

        encoding = {}
        for var in merged.data_vars:
            encoding[var] = {
                'zlib': True,
                'complevel': 4,
                'dtype': 'float32'
            }

        merged.to_netcdf(output_path, encoding=encoding)

        # Cleanup
        for ds in datasets:
            ds.close()
        merged.close()

        result["output_size_mb"] = get_file_size_mb(output_path)
        result["output_file"] = output_name
        result["success"] = True

        reduction = (1 - result["output_size_mb"] / result["input_size_mb"]) * 100
        print(f"done! {result['output_size_mb']:.0f} MB ({reduction:.0f}% reduction)")

    except Exception as e:
        result["message"] = str(e)
        print(f"ERROR: {e}")
        import traceback
        traceback.print_exc()

    return result

def find_and_group_files(input_dir: Path) -> dict:
    """Find all NetCDF files and group by variable."""

    all_files = []

    # Check subdirectories
    for subdir in ['climate', 'water', '']:
        search_dir = input_dir / subdir if subdir else input_dir
        if search_dir.exists():
            all_files.extend(search_dir.glob("*.nc"))

    # Remove duplicates and exclude subsetted
    all_files = [f for f in set(all_files)
                 if "_europe_" not in f.name
                 and "subsetted" not in str(f)]

    # Group by variable
    groups = defaultdict(list)
    for f in all_files:
        key = get_variable_key(f.name)
        groups[key].append(f)

    return dict(groups)

