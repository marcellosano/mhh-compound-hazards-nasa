    # Process each variable group
    print("\n" + "=" * 70)
    print("PROCESSING (subset + merge)")
    print("=" * 70)

    results = []
    for var_key, files in sorted(groups.items()):
        result = process_variable_group(var_key, files, CONFIG.OUTPUT_DIR)
        results.append(result)

    # Summary
    print("\n" + "=" * 70)
    print("SUMMARY")
    print("=" * 70)

    successful = [r for r in results if r["success"]]
    failed = [r for r in results if not r["success"]]

    total_input = sum(r["input_size_mb"] for r in results)
    total_output = sum(r["output_size_mb"] for r in successful)

    print(f"\n✓ Processed: {len(successful)}/{len(results)} variable groups")
    if failed:
        print(f"✗ Failed: {len(failed)}")
        for r in failed:
            print(f"    - {r['variable']}: {r['message']}")

    print(f"\n📊 Size reduction:")
    print(f"   Input:  {total_input / 1024:.2f} GB ({sum(len(r['input_files']) for r in results)} files)")
    print(f"   Output: {total_output / 1024:.2f} GB ({len(successful)} files)")
    print(f"   Saved:  {(total_input - total_output) / 1024:.2f} GB ({(1 - total_output/total_input) * 100:.0f}%)")

    print(f"\n📁 Output files in: {CONFIG.OUTPUT_DIR}")

    if successful:
        print("\nFinal subsetted files:")
        for r in sorted(successful, key=lambda x: x.get('output_file', '')):
            print(f"  • {r.get('output_file', 'unknown')} ({r['output_size_mb']:.0f} MB)")

