def should_skip_file(filepath: Path, remote_size: Optional[int]) -> Tuple[bool, str]:
    """
    Check if file should be skipped.

    Returns:
        (should_skip, reason)
    """
    if not CONFIG.SKIP_EXISTING:
        return False, ""

    if not filepath.exists():
        return False, ""

    local_size = filepath.stat().st_size

    # If we don't know remote size, check if file is non-empty
    if remote_size is None:
        if local_size > 0:
            return True, f"exists ({local_size / 1e6:.1f} MB, remote size unknown)"
        return False, ""

    # Compare sizes
    if local_size == remote_size:
        return True, f"complete ({local_size / 1e6:.1f} MB)"

    # Allow small tolerance (some servers report slightly different sizes)
    if remote_size > 0:
        diff_ratio = abs(local_size - remote_size) / remote_size
        if diff_ratio < CONFIG.SIZE_TOLERANCE:
            return True, f"complete (~{local_size / 1e6:.1f} MB)"

    # File exists but incomplete
    if local_size < remote_size:
        return False, f"incomplete ({local_size / 1e6:.1f} / {remote_size / 1e6:.1f} MB)"

    # Local file larger than remote (shouldn't happen, re-download)
    return False, f"size mismatch (local: {local_size}, remote: {remote_size})"


