def download_file(url: str, filepath: Path, resume: bool = True) -> bool:
    """Download file with progress bar and resume support."""
    logger = logging.getLogger('ISIMIP')

    filepath.parent.mkdir(parents=True, exist_ok=True)

    # Check for partial download
    existing_size = 0
    headers = {}
    mode = 'wb'

    if resume and filepath.exists():
        existing_size = filepath.stat().st_size
        headers = {'Range': f'bytes={existing_size}-'}
        mode = 'ab'

    try:
        time.sleep(CONFIG.DELAY_BETWEEN_REQUESTS)

        response = requests.get(url, headers=headers, stream=True, timeout=CONFIG.TIMEOUT)

        if response.status_code == 416:
            # Range Not Satisfiable = file already complete
            logger.info(f"Already complete: {filepath.name}")
            return True

        response.raise_for_status()

        content_length = response.headers.get('content-length')
        total_size = int(content_length) + existing_size if content_length else 0

        with open(filepath, mode) as f:
            with tqdm(total=total_size, initial=existing_size, unit='B',
                     unit_scale=True, desc=filepath.name[:40], leave=True) as pbar:
                for chunk in response.iter_content(chunk_size=CONFIG.CHUNK_SIZE):
                    if chunk:
                        f.write(chunk)
                        pbar.update(len(chunk))

        return True

    except Exception as e:
        logger.error(f"Download error {filepath.name}: {e}")
        return False


def download_with_retry(url: str, filepath: Path) -> bool:
    """Download with exponential backoff retry."""
    logger = logging.getLogger('ISIMIP')

    for attempt in range(CONFIG.MAX_RETRIES):
        if attempt > 0:
            wait = 5 * (2 ** attempt)
            logger.info(f"Retry {attempt}/{CONFIG.MAX_RETRIES} in {wait}s...")
            time.sleep(wait)

        if download_file(url, filepath):
            return True

    return False


